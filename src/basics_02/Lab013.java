package src.basics_02;

public class Lab013 {
    public static void main(String[] args) {
        double a = 34;
        double b = 10;
        double result = a%b;
        // % -> mod -> Reminder
        System.out.println(result);

        // 10  | 34 | 3 - Q
        //    | 30 |
        //    --------
        //       4


        //  78%12 ->
//        12 | 78 | 6 - Q
//           |  72 |
//               6 - Remainder



        int  num = 38;
        int r = num%2;
        System.out.println(r);

//        num%2 == 0 -> Even
//        num%2 == 1 -> Odd



    }
}
